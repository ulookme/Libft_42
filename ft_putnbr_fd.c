#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	nb;
	char			c;

	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	nb = n;
	c = (nb % 10) + '0';
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	write(fd, &c, 1);
}
