#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int	t;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
	}
	t = n;
	if (t < 0)
	{
		t *= -1;
		ft_putchar_fd('-', fd);
	}
	if (t > 9)
	{
		ft_putnbr_fd((t / 10), fd);
		ft_putchar_fd((t % 10 + '0'), fd);
	}
	else
		ft_putchar_fd((t + '0'), fd);
}
